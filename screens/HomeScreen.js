import React from 'react';
import {
  Image,
  Platform,
  ScrollView,
  StyleSheet,
  Text,
  TouchableOpacity,
  View,
} from 'react-native';
import { WebBrowser } from 'expo';
import MapView, {Marker} from 'react-native-maps';
import { MonoText } from '../components/StyledText';
import { TextInput } from 'react-native-gesture-handler';

export default class HomeScreen extends React.Component {
  constructor(props) {
    super(props);

    this.state = {
      latitude: null,
      longitude: null,
      error: null,
        markers: [],
    };
  }

  componentDidMount() {
    navigator.geolocation.getCurrentPosition(
      (position) => {
        this.setState({
          latitude: position.coords.latitude,
          longitude: position.coords.longitude,
          error: null,
          markers:[{
            title: 'hello',
            coordinates: {
              latitude: position.coords.latitude,
              longitude: position.coords.longitude,
            },
          }]
        });
      },
      (error) => this.setState({ error: error.message }),
      { enableHighAccuracy: true, timeout: 20000, maximumAge: 1000 },
    );
  
  }


  static navigationOptions = {
    header: null,
  };

  render() {
      return (
      <View style={styles.container}>
        <MapView
            style={styles.container} 
            initialRegion={{
              latitude: this.state.latitude,
              longitude: this.state.longitude,
              latitudeDelta: 0.0922,
              longitudeDelta: 0.0421,
            }}
          >
            {this.state.markers.map(marker => (
              <Marker
                coordinate={marker.coordinates}
                title={marker.title}
              />
            ))}                      
          </MapView>
          <View style={{height: 60, width:'100%', position:'absolute', top:40 }}>
          <TextInput
            style={{ marginLeft:'15%', paddingLeft:10, paddingRight:10, height:40, marginRight:'15%', width:'70%', borderColor: 'lightgray', backgroundColor:'rgba(255, 255, 255, 0.9)', borderWidth: 1, borderRadius:15}}
            onChangeText={(text) => this.setState({input: text})}
          />
          <View style={{height:20, width:20, position:'absolute', top:10, right:'17%', backgroundColor:'rgba(200,100,100,0.5)', borderRadius:20 }}/>

        </View>  

      </View>
    );
  }

  _maybeRenderDevelopmentModeWarning() {
    if (__DEV__) {
      const learnMoreButton = (
        <Text onPress={this._handleLearnMorePress} style={styles.helpLinkText}>
          Learn more
        </Text>
      );

      return (
        <Text style={styles.developmentModeText}>
          Development mode is enabled, your app will be slower but you can use useful development
          tools. {learnMoreButton}
        </Text>
      );
    } else {
      return (
        <Text style={styles.developmentModeText}>
          You are not in development mode, your app will run at full speed.
        </Text>
      );
    }
  }

  _handleLearnMorePress = () => {
    WebBrowser.openBrowserAsync('https://docs.expo.io/versions/latest/guides/development-mode');
  };

  _handleHelpPress = () => {
    WebBrowser.openBrowserAsync(
      'https://docs.expo.io/versions/latest/guides/up-and-running.html#can-t-see-your-changes'
    );
  };
}

